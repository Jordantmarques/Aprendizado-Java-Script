// Declaração das variáveis
let fahren;
let celcius;

// Atribuição de valores
fahren = 73;

// Regras de negócio -> Fórmula de conversão de temperatura de celcius para fahrenheit
celcius = ((fahren - 32) / 9) * 5;

// Impressão do resultado
console.log("A temperatura em celcius é de: " + celcius + " °C");