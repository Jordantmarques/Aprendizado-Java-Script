// Declaração de variáveis
let a;
let b;

// Atribuição de variáveis
a = 1;
b = 2;

// Impressões verdadeiras
console.log(a < b);
console.log(a != b);
console.log(a !== b);
console.log(a <= b);
console.log(a + a === b); // Comparação estrita -> Verifica se além de iguais os valores são do mesmo tipo

console.log("\n");

// Impressões falsas
console.log(a == b);
console.log(a > b);
console.log(a == b + a);
console.log(a + b == 0);
console.log(a === b);