function soma(num1, num2) {
    let resutlado = num1 + num2;

    return resutlado;
}

function subtrai(num1, num2) {
    let resutlado = num1 - num2;

    return resutlado;
}

console.log(soma(3, 4));
console.log(soma(31, 98));

console.log('\n');

console.log(subtrai(11, 7));
console.log(subtrai(19, 52));